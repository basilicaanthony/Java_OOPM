public class MathClass {
    public static void main(String[] args) {
        double c=78;
        double d=10;

        System.out.println("Max of c and d is " + Math.max(c,d));
        System.out.println("square root of c and d are "+Math.sqrt(c) +"  "+ Math.sqrt(d));
        System.out.println("power of c and d are "+ Math.pow(c,d));
        System.out.println("log of c  "+Math.log(c));
        System.out.println("log to the base 10 of d is "+ Math.log10(d));
    }
}
// the class you have to import is Java.lang.Math.*;